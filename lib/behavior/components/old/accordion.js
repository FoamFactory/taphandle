var ACCORDION = ".".concat(PREFIX, "-accordion, .").concat(PREFIX, "-accordion-bordered");
var BUTTON = ".".concat(PREFIX, "-accordion-button[aria-controls]");
var EXPANDED = 'aria-expanded';
